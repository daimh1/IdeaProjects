package Day5;

public class Point {
    protected double x;
    protected double y;
    public double getx(){
        return this.x;
    }

    public double gety() {
        return this.y;
    }
    public void setX(double x){
        this.x=x;
    }

    public void setY(double y) {
        this.y = y;
    }
}
